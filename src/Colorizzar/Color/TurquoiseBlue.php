<?php

namespace Colorizzar\Color;

class TurquoiseBlue implements HtmlColor
{
    public function getColorName()
    {
        return 'TurquoiseBlue';
    }

    public function getHex()
    {
        return '#77DDE7';
    }

    public function getRgb()
    {
        return (array) [119, 221, 231];
    }
}
